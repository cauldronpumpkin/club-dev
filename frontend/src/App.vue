<template>
  <v-app>
    <router-view />
  </v-app>
</template>

<script>
import router from "./router";
export default {
  created() {
    this.checkLoggedIn();
  },
  methods: {
    checkLoggedIn() {
      this.$session.start();
      if (!this.$session.has("token")) {
        router.push("/auth");
      }
    }
  }
};
</script>

